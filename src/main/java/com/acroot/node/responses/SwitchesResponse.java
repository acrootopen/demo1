package com.acroot.node.responses;

public class SwitchesResponse {

	Long SwitchesId;
	Long SlavesId;
	boolean Status;
	public Long getSwitchesId() {
		return SwitchesId;
	}
	public void setSwitchesId(Long switchesId) {
		SwitchesId = switchesId;
	}
	public Long getSlavesId() {
		return SlavesId;
	}
	public void setSlavesId(Long slavesId) {
		SlavesId = slavesId;
	}
	public boolean isStatus() {
		return Status;
	}
	public void setStatus(boolean status) {
		Status = status;
	}
	
	
}
